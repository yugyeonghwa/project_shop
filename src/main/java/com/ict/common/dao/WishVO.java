package com.ict.common.dao;

public class WishVO {
	private String wish_idx, p_idx, u_idx, regdate;

	public String getWish_idx() {
		return wish_idx;
	}

	public void setWish_idx(String wish_idx) {
		this.wish_idx = wish_idx;
	}

	public String getP_idx() {
		return p_idx;
	}

	public void setP_idx(String p_idx) {
		this.p_idx = p_idx;
	}

	public String getU_idx() {
		return u_idx;
	}

	public void setU_idx(String u_idx) {
		this.u_idx = u_idx;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	
}
