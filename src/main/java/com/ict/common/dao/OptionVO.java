package com.ict.common.dao;

public class OptionVO {
	private String option_idx, p_idx, p_size, p_quant, regdate;

	public String getOption_idx() {
		return option_idx;
	}

	public void setOption_idx(String option_idx) {
		this.option_idx = option_idx;
	}

	public String getP_idx() {
		return p_idx;
	}

	public void setP_idx(String p_idx) {
		this.p_idx = p_idx;
	}

	public String getP_size() {
		return p_size;
	}

	public void setP_size(String p_size) {
		this.p_size = p_size;
	}

	public String getP_quant() {
		return p_quant;
	}

	public void setP_quant(String p_quant) {
		this.p_quant = p_quant;
	}

	public String getRegdate() {
		return regdate;
	}

	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}

	
}
